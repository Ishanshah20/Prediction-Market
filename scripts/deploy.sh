#!/usr/bin/env bash
# ============================================================
# deploy.sh - Deploy all Prediction Market contracts to Stellar
# Network: Testnet (Futurenet / Testnet)
# Requirements: stellar CLI, jq
# Usage: ./scripts/deploy.sh
# ============================================================

set -euo pipefail

# ── Config ──────────────────────────────────────────────────
NETWORK="${STELLAR_NETWORK:-testnet}"
SOURCE_ACCOUNT="${STELLAR_SOURCE_ACCOUNT:-alice}"
RPC_URL="${STELLAR_RPC_URL:-https://soroban-testnet.stellar.org}"
NETWORK_PASSPHRASE="${STELLAR_NETWORK_PASSPHRASE:-Test SDF Network ; September 2015}"

echo "🚀 Deploying Prediction Market Platform to Stellar $NETWORK"
echo "   Source account: $SOURCE_ACCOUNT"
echo "   RPC URL: $RPC_URL"
echo ""

# ── Build contracts ─────────────────────────────────────────
echo "📦 Building contracts..."
stellar contract build

# ── Deploy PredictionToken ───────────────────────────────────
echo ""
echo "1️⃣  Deploying PredictionToken..."
TOKEN_ADDRESS=$(stellar contract deploy \
  --wasm target/wasm32-unknown-unknown/release/prediction_token.wasm \
  --source "$SOURCE_ACCOUNT" \
  --network "$NETWORK" \
  --rpc-url "$RPC_URL" \
  --network-passphrase "$NETWORK_PASSPHRASE")

echo "   ✅ PredictionToken deployed: $TOKEN_ADDRESS"

# ── Initialize PredictionToken ───────────────────────────────
echo "   Initializing PredictionToken..."
ADMIN_ADDRESS=$(stellar keys address "$SOURCE_ACCOUNT")

stellar contract invoke \
  --id "$TOKEN_ADDRESS" \
  --source "$SOURCE_ACCOUNT" \
  --network "$NETWORK" \
  --rpc-url "$RPC_URL" \
  --network-passphrase "$NETWORK_PASSPHRASE" \
  -- initialize \
  --admin "$ADMIN_ADDRESS" \
  --decimal 7 \
  --name "PredictionToken" \
  --symbol "PRED"

echo "   ✅ PredictionToken initialized with 1,000,000 PRED minted to $ADMIN_ADDRESS"

# ── Deploy PredictionMarket WASM (for factory) ───────────────
echo ""
echo "2️⃣  Uploading PredictionMarket WASM..."
MARKET_WASM_HASH=$(stellar contract upload \
  --wasm target/wasm32-unknown-unknown/release/prediction_market.wasm \
  --source "$SOURCE_ACCOUNT" \
  --network "$NETWORK" \
  --rpc-url "$RPC_URL" \
  --network-passphrase "$NETWORK_PASSPHRASE")

echo "   ✅ PredictionMarket WASM hash: $MARKET_WASM_HASH"

# ── Deploy MarketFactory ─────────────────────────────────────
echo ""
echo "3️⃣  Deploying MarketFactory..."
FACTORY_ADDRESS=$(stellar contract deploy \
  --wasm target/wasm32-unknown-unknown/release/market_factory.wasm \
  --source "$SOURCE_ACCOUNT" \
  --network "$NETWORK" \
  --rpc-url "$RPC_URL" \
  --network-passphrase "$NETWORK_PASSPHRASE")

echo "   ✅ MarketFactory deployed: $FACTORY_ADDRESS"

# ── Initialize MarketFactory ─────────────────────────────────
echo "   Initializing MarketFactory..."
stellar contract invoke \
  --id "$FACTORY_ADDRESS" \
  --source "$SOURCE_ACCOUNT" \
  --network "$NETWORK" \
  --rpc-url "$RPC_URL" \
  --network-passphrase "$NETWORK_PASSPHRASE" \
  -- initialize \
  --admin "$ADMIN_ADDRESS" \
  --token_address "$TOKEN_ADDRESS" \
  --market_wasm_hash "$MARKET_WASM_HASH"

echo "   ✅ MarketFactory initialized"

# ── Create a sample market ───────────────────────────────────
echo ""
echo "4️⃣  Creating sample prediction market..."
DEADLINE=$(($(date +%s) + 86400))  # 24 hours from now

SAMPLE_MARKET=$(stellar contract invoke \
  --id "$FACTORY_ADDRESS" \
  --source "$SOURCE_ACCOUNT" \
  --network "$NETWORK" \
  --rpc-url "$RPC_URL" \
  --network-passphrase "$NETWORK_PASSPHRASE" \
  -- create_market \
  --creator "$ADMIN_ADDRESS" \
  --question "Will XLM reach \$1 by end of 2025?" \
  --deadline "$DEADLINE")

echo "   ✅ Sample market created: $SAMPLE_MARKET"

# ── Save addresses ───────────────────────────────────────────
echo ""
echo "💾 Saving contract addresses..."
cat > .env.contracts << EOF
# Auto-generated by deploy.sh - $(date)
VITE_NETWORK=$NETWORK
VITE_RPC_URL=$RPC_URL
VITE_NETWORK_PASSPHRASE=$NETWORK_PASSPHRASE
VITE_TOKEN_CONTRACT_ID=$TOKEN_ADDRESS
VITE_FACTORY_CONTRACT_ID=$FACTORY_ADDRESS
VITE_MARKET_WASM_HASH=$MARKET_WASM_HASH
VITE_ADMIN_ADDRESS=$ADMIN_ADDRESS
EOF

# Also update frontend .env
cp .env.contracts frontend/.env

echo ""
echo "════════════════════════════════════════════════════════"
echo "✅ Deployment Complete!"
echo "════════════════════════════════════════════════════════"
echo "  Network:          $NETWORK"
echo "  PredictionToken:  $TOKEN_ADDRESS"
echo "  MarketFactory:    $FACTORY_ADDRESS"
echo "  Market WASM Hash: $MARKET_WASM_HASH"
echo "  Sample Market:    $SAMPLE_MARKET"
echo "════════════════════════════════════════════════════════"
echo ""
echo "📝 Addresses saved to .env.contracts and frontend/.env"
